
#include <iostream>
using namespace std;

int main()
{
    int num1, num2, sum;

    // Taking input from user
    cout << "Enter first number: ";
    cin >> num1;

    cout << "Enter second number: ";
    cin >> num2;

    // Adding the numbers
    sum = num1 + num2;

    // Displaying the result
    cout << "Sum = " << sum << endl;

    return 0;
}
